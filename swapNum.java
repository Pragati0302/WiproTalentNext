class swapNum{
    public static void main(String[] args) {
        int x,y,temp;
        x = 10;
        y = 20;
        System.out.println("Before SWapping:\n x= "+x+" y= "+y);
        temp = x;
        x = y;
        y = temp;
        System.out.println("After Swapping:\n x = "+x+" y= "+y); 
    }
}
